          .div(titulo="Diseño hidrológico en línea clave")
            p El diseño en línea clave (<em>keyline</em>) es una herramienta para organizar el paisaje rural y aprovechar mejor el agua, mejorando la fertilidad del suelo. Se basa en identificar las curvas de nivel y planificar la ubicación de cultivos, caminos, cercas y zanjas en desniveles inferiores al 3 % de pendiente. Este método permite que el agua de lluvia se infiltre en el suelo en lugar de escurrirse, evitando erosión y encharcamientos. En zonas con pendiente, ayuda a conservar el agua, reducir el desgaste del suelo y aumentar la productividad sin depender de riego artificial. Además, fortalece la resiliencia del agroecosistema frente a la variabilidad climática y puede integrarse con prácticas comunitarias como la cosecha de agua y la reforestación de nacederos (Cortés, 2013).
    
          .div(titulo="Diseño permacultural")
            p La <em>permacultura</em> propone diseñar fincas y territorios como sistemas vivos donde cada elemento cumple varias funciones e interactúa con los demás. No se trata solo de sembrar, sino de planificar los espacios, flujos de energía, materiales y relaciones humanas. Sus principios incluyen “observar e interactuar”, “producir sin desperdiciar”, “cuidar la tierra y las personas” y “compartir lo que sobra”. En la práctica, incorpora técnicas como huertos en espiral, cultivos en terrazas, uso de plantas compañeras, recolección de agua lluvia e integración de animales. Este enfoque promueve la autonomía familiar y comunitaria mediante el diseño de espacios sostenibles, diversos y adaptados a las condiciones locales (Alakendu et al., 2024). Resulta valioso en procesos educativos, ya que permite comprender el agroecosistema como un sistema interconectado.
    
          .div(titulo="Diseño con base en la memoria biocultural")
            p Este enfoque reconoce que el territorio guarda no solo recursos naturales, sino también historias, saberes y prácticas construidas colectivamente. Diseñar desde la memoria biocultural implica recuperar conocimientos ancestrales, formas tradicionales de cultivo, uso de plantas medicinales, manejo del agua y organización del paisaje. También conlleva respetar los nombres, rituales, recorridos y formas de vida que otorgan sentido al territorio. En contextos campesinos e indígenas, este enfoque garantiza que el diseño del agroecosistema no reemplace lo existente, sino que lo fortalezca. Puede implementarse mediante mapeos participativos, cartografías sociales, inventarios de saberes y encuentros intergeneracionales (Toledo y Barrera-Bassols, 2023). Es una forma de planear con el corazón del territorio, reconociendo que la sostenibilidad depende tanto de lo técnico como de lo cultural y lo espiritual.







